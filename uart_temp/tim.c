#include <STC15F2K60S2.H>

void Cls_Peripheral(void){
  P0 = 0xFF;
  P2 = P2 & 0x1F | 0x80;			// P27~P25清零，再定位Y4C
  P2 &= 0x1F;						// P27~P25清零
  P0 = 0;
  P2 = P2 & 0x1F | 0xA0;			// P27~P25清零，再定位Y5C
  P2 &= 0x1F;						// P27~P25清零
}

void Timer0Init(void)	//（我们的T0用的是16位自动重装定时器模式）
{
	TMOD=0x00;// 16位自动重装
	TL0 = (65536-1000)%256;
	TH0 = (65536-1000)/256;
	EA=1;   //打开全局中断
	ET0=1;  //打开定时器1中断
	//TF0 = 0;//中断溢出清零，对于TMOD=0x00模式来说，该操作由系统自动完成
	TR0 = 1;//开启定时器0
}